def _path_without_executable(name: str) -> str:
    parts: list[str] = []
    for raw in os.environ.get("PATH", "").split(os.pathsep):
        if not raw:
            continue
        candidate = Path(raw) / name
        if candidate.exists():
            continue
        parts.append(raw)
    return os.pathsep.join(parts)


def _run_aicodex_with_stub(
    tmp_path: Path,
    args: list[str],
    env_extra: dict[str, str | None] | None = None,
    env_model: str | None = "env-local-model",
    with_codex: bool = True,
    preexisting_wrapper: str | None = None,
    project_name: str = "project",
) -> tuple[subprocess.CompletedProcess[str], dict[str, Path]]:
    if not shutil.which("git"):
        pytest.skip("git is required for the aicodex wrapper tests")
    bash = _require_working_bash()
    aicodex_script = _bash_compatible_path(bash, REPO_ROOT / "aicodex")

    project = tmp_path / project_name
    project.mkdir()
    subprocess.run(["git", "init", "-q"], cwd=project, check=True)
    subdir = project / "src"
    subdir.mkdir()
    home = tmp_path / "home"
    home.mkdir()
    bin_dir = tmp_path / "bin"
    bin_dir.mkdir()
    args_file = tmp_path / "codex_args.txt"
    env_file = tmp_path / "codex_env.txt"

    if preexisting_wrapper is not None:
        wrapper = project / ".codex" / "run-codetrail-mcp"
        wrapper.parent.mkdir(parents=True, exist_ok=True)
        wrapper.write_text(preexisting_wrapper, encoding="utf-8")
        wrapper.chmod(0o700)

    if with_codex:
        stub_codex = bin_dir / "codex"
        stub_codex.write_text(
            "#!/usr/bin/env bash\n"
            "set -euo pipefail\n"
            ": > \"$AICODEX_FAKE_CODEX_ARGS\"\n"
            "for arg in \"$@\"; do\n"
            "  printf '%s\\n' \"$arg\" >> \"$AICODEX_FAKE_CODEX_ARGS\"\n"
            "done\n"
            "{\n"
            "  printf 'PWD=%s\\n' \"$PWD\"\n"
            "  printf 'AICODE_ROOT=%s\\n' \"${AICODE_ROOT:-}\"\n"
            "  printf 'CODETRAIL_HOME=%s\\n' \"${CODETRAIL_HOME:-}\"\n"
            "  printf 'AICODE_MODEL=%s\\n' \"${AICODE_MODEL:-}\"\n"
            "} > \"$AICODEX_FAKE_CODEX_ENV\"\n",
            encoding="utf-8",
        )
        stub_codex.chmod(0o700)

    env = os.environ.copy()
    for key in ("AICODE_MODEL", "AICODE_ROOT", "OPENCODE_CONFIG"):
        env.pop(key, None)
    base_path = os.environ.get("PATH", "") if with_codex else _path_without_executable("codex")
    env.update(
        {
            "HOME": str(home),
            "USERPROFILE": str(home),
            "PATH": f"{bin_dir}{os.pathsep}{base_path}",
            "PYTHONIOENCODING": "utf-8",
            "AICODE_CTX_SAFETY_DISABLE": "1",
            "AICODEX_FAKE_CODEX_ARGS": str(args_file),
            "AICODEX_FAKE_CODEX_ENV": str(env_file),
            "AICODE_REQUIRED_MODELS_CHECK_SKIP": "1",
        }
    )
    if env_model is not None:
        env["AICODE_MODEL"] = env_model
    if env_extra:
        for key, value in env_extra.items():
            if value is None:
                env.pop(key, None)
            elif value == "__HOME__":
                env[key] = str(home)
            else:
                env[key] = value

    result = subprocess.run(
        [bash, aicodex_script, *args],
        cwd=subdir,
        capture_output=True,
        text=True,
        timeout=20,
        stdin=subprocess.DEVNULL,
        env=env,
    )
    return result, {
        "project": project,
        "subdir": subdir,
        "home": home,
        "args_file": args_file,
        "env_file": env_file,
        "wrapper": project / ".codex" / "run-codetrail-mcp",
    }


def _contains_subsequence(items: list[str], expected: list[str]) -> bool:
    if not expected:
        return True
    width = len(expected)
    return any(items[i : i + width] == expected for i in range(len(items) - width + 1))


def _read_key_values(path: Path) -> dict[str, str]:
    if not path.exists():
        return {}
    out: dict[str, str] = {}
    for line in path.read_text(encoding="utf-8").splitlines():
        if "=" in line:
            key, value = line.split("=", 1)
            out[key] = value
    return out


